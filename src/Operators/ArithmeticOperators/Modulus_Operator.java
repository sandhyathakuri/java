package Operators.ArithmeticOperators;

public class Modulus_Operator {
    public static void main(String[] args) {
        int a = 3 % 2;
        int b = 11 % 3;
        int c = 7 % -3;


        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        double d = 2.4;
        double e = 0.2;
        System.out.println(d % e);
    }
}
